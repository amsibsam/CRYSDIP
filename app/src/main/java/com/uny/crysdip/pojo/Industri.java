public class Industri {
    private final String nama;
    private final String alamat;

    public Industri(String nama, String alamat) {
        this.nama = nama;
        this.alamat = alamat;
    }

    public String getNama() {
        return nama;
    }

    public String getAlamat() {
        return alamat;
    }


}
